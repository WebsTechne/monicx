// src/app/layout.tsx
import type { ReactNode } from "react";
import type { Metadata } from "next";
import "./globals.css";
import localFont from "next/font/local";
import LayoutContent from "@/components/layout-content";

import { getCategories } from "@/lib/get-categories";
import { getCollections } from "@/lib/get-collections";
import { getSizes } from "@/lib/get-sizes";
import { getColors } from "@/lib/get-colors";

export const metadata: Metadata = {
    title: "Create Next App",
    description: "Generated by create next app",
};

const nunitoSans = localFont({
    src: [
        {
            path: "../assets/fonts/nunito-sans-variable.ttf",
            style: "normal",
        },
        {
            path: "../assets/fonts/nunito-sans-italic-variable.ttf",
            style: "italic",
        },
    ],
    variable: "--font-nunito-sans",
});

export default async function RootLayout({
    children,
}: Readonly<{
    children: ReactNode;
}>) {
    const [categories, collections, sizes, colors] = await Promise.all([
        getCategories(),
        getCollections(),
        getSizes(), // can be a simple exported constant or DB fetch
        getColors(), // same
    ]);

    return (
        <html lang="en" suppressHydrationWarning>
            <head />
            <body className={`${nunitoSans.variable} antialiased`}>
                {
                    <LayoutContent
                        initialCategories={categories}
                        initialCollections={collections}
                        initialSizes={sizes}
                        initialColors={colors}
                    >
                        {children}
                    </LayoutContent>
                }
            </body>
        </html>
    );
}
